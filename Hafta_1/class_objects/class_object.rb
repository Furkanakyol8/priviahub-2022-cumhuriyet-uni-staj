# ruby'de bir sınıf ve nesne üretmeyi görelim

class Cikolata
    def ye
        puts "Tadı çok güzel!"
    end
end

# Cikolata isimli bir class oluşturduk ve 'ye' isimli bir method tanımladık.

my_cikolata = Cikolata.new
my_cikolata.ye

# my_cikolata isimli bir nesne oluşturduk ve 'ye' metodunu nesne üzerinden kullandık...