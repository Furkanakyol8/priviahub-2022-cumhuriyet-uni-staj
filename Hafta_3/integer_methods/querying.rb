# allbits? Metotu: İçindeki tüm bitlerin selfayarlanıp ayarlanmadığını döndürür.

0b1010101  self
0b1010100  mask
0b1010100  self & mask
true  self.allbits?(mask)  


# anybits? Metotu: Herhangi bir bitin selfayarlanıp ayarlanmadığını döndürür.

0b10000010  self
0b11111111  mask
0b10000010  self & mask
true  self.anybits?(mask)
 

